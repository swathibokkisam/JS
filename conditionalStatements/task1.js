var day="monday";
switch(day){
  case monday:  
  console.log("Start your week strong!");
  break;
  case tuesday:
  console.log("Keep going!");
  break;
  case wednesday:
  console.log("Halfway there!");
  break;
  case thursday:
  console.log("Almost the weekend!");
  break;
  case friday:
  console.log("Happy Friday!");
  break;
  case saturday && sunday:
  console.log("Enjoy your weekend!");
  break;

  default:
    console.log("nothing")
}
